package colors;

public class ConsoleColors {
    // Reset
    public static final String ANSI_RESET = "\u001B[0m";

    // Colors
    public static final String ANSI_YELLOW = "\u001B[33m"; // Used for application welcome message
    public static final String ANSI_BLUE = "\u001B[34m"; // Used for user input menus
    public static final String ANSI_PURPLE = "\u001B[35m"; // Used for current menu title
    public static final String ANSI_GREEN = "\u001B[32m"; // Used for success messages
}

